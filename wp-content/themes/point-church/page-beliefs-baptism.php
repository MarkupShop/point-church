			<h5>What if I was baptized as an infant?</h5>

			<p>Some churches baptize infants. At The Point Church we don't. We only baptize those who have made a decision to believe in Christ's death as the payment for their sin. In the Bible, men and women were baptized following their belief in Jesus.</p>

			<p>The custom of infant baptism began about 300 years after the Bible was completed. It's a ceremony intended to emphasize a commitment between the parents and God on the behalf of the child. During this ceremony, the parents promise to raise their child in the faith until the child is old enough to make his own personal confession of Christ.</p>

			<p>At The Point Church, we have a dedication service for infants and children that emphasize the parent's role in raising a child to follow Christ.</p>

			<p>Therefore, if you were baptized as a baby, we encourage you to make your own choice to be baptized as a believer.</p>
 
